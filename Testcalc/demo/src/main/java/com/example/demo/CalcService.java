package com.example.demo;

public class CalcService {
    public int calcadd(int nummer1, int nummer2){
        int addSum = nummer1 + nummer2;
        return addSum;
    }
    public int calcsub(int nummer1, int nummer2){
        int subSum = nummer1 - nummer2;
        return subSum;
    }
    public int calcmult(int nummer1, int nummer2){
        int multSum = nummer1 * nummer2;
        return multSum;
    }
    public int calcdiv(int nummer1, int nummer2){
        int divSum = nummer1 / nummer2;
        return divSum;
    }
}
